#include "libft.h"

int ft_strcmp(const char *s1, const char *s2)
{
    size_t i;

    if (!s1 || !s2)
        return (-1); // Return an error code instead of NULL

    i = 0;
    while (s1[i] && s2[i])
    {
        if (s1[i] != s2[i])
            return ((unsigned char)s1[i] - (unsigned char)s2[i]);
        i++;
    }
    return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
